      Algunos de los riesgos a los que una persona se expone al trabajar como programador </br>
      desde la modalidad de teletrabajo son los siguientes:
    </p>
  </main>

  <div class="main-container">
  <div class="container-text">
    <ul>
      <li>
        Aislamiento social: La falta de interacción física con colegas puede llevar a sentirse aislado, lo que puede
        afectar la salud mental y el bienestar general.
      </li>
      <li>Falta de límites: Trabajar desde casa puede dificultar establecer límites claros entre el trabajo y la vida
        personal. Esto puede resultar en una mayor carga de trabajo, estrés y dificultad para desconectar.</li>
      <li>Ergonomía deficiente: Al no tener un entorno de trabajo adecuado, como una silla ergonómica o una
        configuración
        adecuada del escritorio, se pueden experimentar problemas físicos como dolores de espalda, cuello y muñecas.
      </li>
      <li>Distracciones en el hogar: Estar rodeado de distracciones domésticas, como tareas del hogar, familiares o
        mascotas, puede dificultar la concentración y disminuir la productividad.
      </li>
      <li>Problemas de comunicación: La comunicación remota puede presentar desafíos, como malentendidos o dificultades
        para expresar ideas de manera efectiva, lo que puede afectar la colaboración con compañeros de trabajo y
        clientes.
      </li>
    </ul>
